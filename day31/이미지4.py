psize=10

def make(x,y,psize,pcol):
    turtle.speed(0)
    turtle.penup()
    turtle.goto(x*psize,(-1)*y*psize)
    turtle.pendown()
    turtle.begin_fill()
    turtle.fillcolor(pcol)
    turtle.setheading(45)
    turtle.circle(psize/2,steps=4)
    turtle.end_fill()

for j in range(0,16):
    for i in range(0,16):
        if addImg[j][i]>1:
            pcol='red'
            make(i,j,psize,pcol)
        elif addImg[j][i]>0:
            pcol='orange'
            make(i,j,psize,pcol)
        else:
            pcol='white'
            make(i,j,psize,pcol)
            
